int	ft_sqrt(int nb)
{
	int	i;
	int	res;

	i = 1;
	while ((i * i == nb) || (i <= 46340))
	{
		res = i * i;
		if (res == nb)
		{
			return (i);
		}
		i ++;
	}
	return (0);
}
